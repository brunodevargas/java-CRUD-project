    //configuraÃ§Ã£o do botÃ£o deletar
    private void botaoDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoDeletarActionPerformed
        //Confirmando a aÃ§Ã£o do usuario
        int resposta_usuario = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja APAGAR esta tarefa ?");
        //Se clicar em sim vai carregar o if abaixo
        if (resposta_usuario == 0) {
            String numero = numeroTarefa.getText();
            int numerotarefa = Integer.parseInt(numero);

            if (this.tarefa.DeleteTarefaBD(numerotarefa)) {
                JOptionPane.showMessageDialog(rootPane, "Tarefa Apagada com Sucesso!");
            }
            //fecha a tela de mostrar tabela
            TelaPrincipal reload = new TelaPrincipal();
            reload.setVisible(true);
            this.setVisible(false);
        }
    }//GEN-LAST:event_botaoDeletarActionPerformed

